package b2018m832;

public class Dots {
	int x;
	int y;
	String text;
	
	public Dots(int x,int y,String text)
	{
		this.x=x;
		this.y=y;
		this.text=text;
	}
	
	public int getX()
	{
		return x;
	}
	public int getY()
	{
		return y;
	}
	public String getText()
	{
		return text;
	}
	
}

